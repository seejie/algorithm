const arr = [1, 6, 7, 4, 5, 8, 9, 0, 2, 3]

const bubbleSort = arr => {
  const len = arr.length
  for (let i = 0; i < len; i++) {
    for (let j = 0; j < len - i - 1; j++) {
      if (arr[j] > arr[j + 1]) {
        [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]]
      }
      // console.log(`第${i+1}次循环`, arr)
    }
  }
  return arr
}

console.log(bubbleSort(arr), '-----bubbleSort(arr)-----')
